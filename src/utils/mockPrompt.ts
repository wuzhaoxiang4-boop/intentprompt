/**
 * 检测输入类型，用于生成更有针对性的通用 Prompt
 */
function detectInputType(input: string): string {
  if (/^我想|[吗？?]$|如何|怎么|怎样/.test(input)) return "question";
  if (/^帮我|请|求|需要/.test(input)) return "request";
  if (/介绍|什么|是啥|什么是/.test(input)) return "explain";
  return "general";
}

